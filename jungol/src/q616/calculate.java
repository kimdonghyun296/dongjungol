public class calculate {
	private double num1;
	private double num2;
	private double num3;
	private double num4;
	private double num5;
	private double num6;
	
	
	
	public void getnum(int a,int b,int c,int d,int e,int f)
	{
		this.num1=a;
		this.num2=b;
		this.num3=c;
		this.num4=d;
		this.num5=e;
		this.num6=f;
	}
	
	public double xre()
	{
		return (num1+num3+num5)/3;
	}
	
	public double yre()
	{
		return (num2+num4+num6)/3;
	}
	
	

}
